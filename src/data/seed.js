export function calcValidationMetrics(startup) {
  const interviews = startup.validation?.interviewSessions || []
  const sessionCount = interviews.reduce((sum, s) => sum + (s.sessions || 0), 0)
  const legacyInterviews = startup.interviews || []
  const completed = sessionCount || legacyInterviews.filter((i) => i.status === 'completed').length
  const pending = legacyInterviews.filter((i) => i.status === 'pending').length

  const assumptions = startup.assumptions || []
  const untested = ['Untested', 'Not Tested']
  const validatedStatuses = ['Validated', 'Evidence Collected']
  const tested = assumptions.filter((a) => !untested.includes(a.status)).length
  const validated = assumptions.filter((a) => validatedStatuses.includes(a.status)).length
  const score = assumptions.length
    ? Math.round(((validated / assumptions.length) * 0.6 + (tested / assumptions.length) * 0.4) * 100)
    : 0
  return { completed, pending, tested, validated, score }
}
